function OUT = PKeq(t,X)

mu1 = -0.38;
mu2 = 0.38;
c1 = -1;
c2 = 1;
a1 = -1.31;
a2 = -2.6;
b1 = 1.504;
b2 = 0.22;
e2 = 1.61;

x1 = X(1);
x2 = X(2);
y1 = X(3);
y2 = X(4);

dx1 = a1*x1^3 + a1*x1*x2^2 + b1*x1*y1^2 + c1*x1*y1 + b1*x1*y2^2 + mu1*x1 + c1*x2*y2;
dx2 = a1*x1^2*x2 + c1*x1*y2 + a1*x2^3 + b1*x2*y1^2 - c1*x2*y1 + b1*x2*y2^2 + mu1*x2;
dy1 = a2*x1^2*y1 + c2*x1^2 + a2*x2^2*y1 - c2*x2^2 + b2*y1^3 + b2*y1*y2^2 + mu2*y1 + e2*y2;
dy2 = a2*x1^2*y2 + 2*c2*x1*x2 + a2*x2^2*y2 + b2*y1^2*y2 - e2*y1 + b2*y2^3 + mu2*y2;

Q = [[X(5), X(9),  X(13), X(17)];
     [X(6), X(10), X(14), X(18)];
     [X(7), X(11), X(15), X(19)];
     [X(8), X(12), X(16), X(20)]];

J = [[ 3*a1*x1^2 + a1*x2^2 + b1*y1^2 + c1*y1 + b1*y2^2 + mu1, c1*y2 + 2*a1*x1*x2, c1*x1 + 2*b1*x1*y1, c1*x2 + 2*b1*x1*y2];...
     [c1*y2 + 2*a1*x1*x2, a1*x1^2 + 3*a1*x2^2 + b1*y1^2 - c1*y1 + b1*y2^2 + mu1, 2*b1*x2*y1 - c1*x2, c1*x1 + 2*b1*x2*y2];...
     [2*c2*x1 + 2*a2*x1*y1, 2*a2*x2*y1 - 2*c2*x2, a2*x1^2 + a2*x2^2 + 3*b2*y1^2 + b2*y2^2 + mu2, e2 + 2*b2*y1*y2];...
     [2*c2*x2 + 2*a2*x1*y2, 2*c2*x1 + 2*a2*x2*y2, 2*b2*y1*y2 - e2, a2*x1^2 + a2*x2^2 + b2*y1^2 + 3*b2*y2^2 + mu2]];

F = J*Q;

OUT = [dx1; dx2; dy1; dy2; F(:)];