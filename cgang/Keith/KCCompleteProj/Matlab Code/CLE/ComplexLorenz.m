function [ k ] = ComplexLorenz(x)
    s = 10;
    b = 8/3;
    r1 = 28;
    r2 = 0;
    e = 1/10;
    k(1:1,1) = -s*x(1:1,1)+s*x(1:1,3);
    k(1:1,2) = -s*x(1:1,2)+s*x(1:1,4);
    k(1:1,3) = (r1-x(1:1,5))*x(1:1,1)-r2*x(1:1,2)-x(1:1,3)-e*x(1:1,4);
    k(1:1,4) = (r1-x(1:1,5))*x(1:1,2)+r2*x(1:1,1)-x(1:1,4)+e*x(1:1,3);
    k(1:1,5) = -b*x(1:1,5)+x(1:1,1)*x(1:1,3)+x(1:1,2)*x(1:1,4);
end